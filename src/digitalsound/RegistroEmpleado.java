    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        TipoEmpleado seleccion = (TipoEmpleado) cb_tipo.getSelectedItem();
        if(txt_nombre.getText().length() == 0 || txt_contraseña.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Ingrese el nombre y/o contraseña del empleado");
        }else{
            Connection con = null;
        PreparedStatement pst = null;
        try{
            con = dsm.getDataSource().getConnection();
            pst = con.prepareStatement("CALL insercion_empleado(?,?,?)");
            pst.setString(1, txt_nombre.getText().trim());
            pst.setString(2, txt_contraseña.getText().trim());
            pst.setInt(3, seleccion.getIdTipo());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Empleado creado exitosamente");
            mConsultaEmpleados.llenarTEmpleados();
        }catch(SQLException e){
            e.printStackTrace();
        }
        }
        
    }//GEN-LAST:event_btn_registrarActionPerformed

