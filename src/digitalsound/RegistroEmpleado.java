    //METODOS PARA LLENAR LA COMBO BOX DE CORREOS
    public ArrayList getListaCorreos() throws SQLException{
                ArrayList mListaTipos = new ArrayList();
                TipoEmpleado mTipoEmpleado = null;
                Connection con = null;
                PreparedStatement pst = null;
                ResultSet rs = null;
                try{
                    con = dsm.getDataSource().getConnection();
                    pst = con.prepareStatement("CALL consulta_tipo_empleado()");
                    rs = pst.executeQuery();
                        while(rs.next()){
                        System.out.println("Procesando fila...");
                        mTipoEmpleado = new TipoEmpleado();
                        mTipoEmpleado.setIdTipo(rs.getInt("id"));
                        mTipoEmpleado.setNombreTipo(rs.getString("nombre"));
                        mListaTipos.add(mTipoEmpleado);
                        }
                        System.out.println("Se ha procesado todo el ResultSet");
                }catch(SQLException e){
                    e.printStackTrace();
                }finally{
                    if(rs != null){
                        rs.close();
                        System.out.println("Conexion resultado: "+rs);
                    }
                    if(pst != null){
                        pst.close();
                        System.out.println("Conexion pst: "+pst);
                    }
                    if(con != null){
                        con.close();
                        System.out.println("Conexion con: "+con);
                    }
                }
        return mListaTipos;
        }
    
        public void llenarCBTipos(){
        cb_tipo.removeAllItems();
        try{
            mListaTipos = this.getListaCorreos();
        }catch(SQLException e){
            e.printStackTrace();
        }
        Iterator iterador = mListaTipos.iterator();
        while (iterador.hasNext()) {
            TipoEmpleado mTipos = (TipoEmpleado) iterador.next();
            if (mTipos != null) {
                cb_tipo.addItem(mTipos);
            }
        }
        }
