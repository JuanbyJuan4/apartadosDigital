    public void llenarLApartados() {
            // Crea un DefaultTableModel con los nombres de las columnas
           DefaultTableModel tableModel = new DefaultTableModel(new String[]{"ID","CLIENTE", "FECHA", "TOTAL", "SALDO"}, 0) {
        @Override
        // Bloquea todas las celdas para que no sean editables
        public boolean isCellEditable(int row, int column) {
            return false; 
        }
        };
           try{
               mListaApartados = getListaApartados();
           }catch(SQLException e){
               e.printStackTrace();
           }
            Iterator<Ticket> iterador = mListaApartados.iterator();
            while (iterador.hasNext()) {
                Ticket mTicket = iterador.next();
                System.out.println("Agregando Ticket: " + mTicket);
                tableModel.addRow(new Object[]{mTicket.getIdTicket(), mTicket.getCliente(), mTicket.getFecha(), mTicket.getTotal(), mTicket.getSaldo()});
            }
            tb_apartados.setModel(tableModel);
        }
        public ArrayList getListaApartados() throws SQLException{
        ArrayList mListaApartados = new ArrayList();
        Ticket mTicket = null;
        Connection con = null;
        PreparedStatement pst = null;
        ResultSet rs = null;
        try{
            
            con = dsm.getDataSource().getConnection();
            pst = con.prepareStatement("CALL consulta_apartados();");
            rs = pst.executeQuery();
            
            while(rs.next()){
                System.out.println("Procesando fila...");
                mTicket = new Ticket();
                mTicket.setIdTicket(rs.getInt("ID"));
                mTicket.setCliente(rs.getString("CLIENTE"));
                mTicket.setFecha(rs.getString("FECHA"));
                mTicket.setTotal(rs.getString("TOTAL"));
                mTicket.setSaldo(rs.getString("SALDO"));
                mListaApartados.add(mTicket);                
            }
            System.out.println("Se ha procesado todo el ResultSet");
        }catch(SQLException e){
            e.printStackTrace();
        }
        finally{
            if(rs != null){
                rs.close();
            }
            if(pst != null){
                pst.close();
            }
            if(con != null){
                con.close();
            }
        }
        return mListaApartados;
    }
    
        //METODO PARA LLENAR LA TABLA tb_productos
        public void llenarLProductos(int id) {
            // Crea un DefaultTableModel con los nombres de las columnas
           DefaultTableModel tableModel = new DefaultTableModel(new String[]{"PRODUCTO", "PRECIO UNITARIO", "CANTIDAD", "TOTAL", "SALDO"}, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Bloquea todas las celdas para que no sean editables
        }
        };
            // Obtiene la lista de equipos de la base de datos
            try{
                mListaProductos = getListaProductos(id);
            }catch(SQLException e){
                e.printStackTrace();
            }
            // Itera sobre la lista de equipos y a침ade cada uno al tableModel
            Iterator<ProductoApartado> iterador = mListaProductos.iterator();
            while (iterador.hasNext()) {
                mProductoApartado = iterador.next();
                System.out.println("Agregando Ticket: " + mProductoApartado);

                // A침ade una fila con los datos de cada objeto Equipos
                tableModel.addRow(new Object[]{mProductoApartado.getNombre(), mProductoApartado.getPrecio(), mProductoApartado.getCantidad(), mProductoApartado.getTotal(), mProductoApartado.getSaldo()});
            }

            // Asigna el DefaultTableModel a la JTable
            tb_productos.setModel(tableModel);
        }
        public ArrayList getListaProductos(int estado) throws SQLException{
            ArrayList mListaProductos = new ArrayList();
            ProductoApartado mProductoApartado = null;
            Connection con = null;
            PreparedStatement pst = null;
            ResultSet rs = null;
            try{
                con = dsm.getDataSource().getConnection();
                pst = con.prepareStatement("CALL buscar_producto_apartado(?);");
                pst.setInt(1, id); //Obtiene el nombre para a침adirlo a la consulta
                lbl_id.setText(""+id);
                rs = pst.executeQuery();
                while(rs.next()){
                    System.out.println("Procesando fila...");
                    mProductoApartado = new ProductoApartado();
                    mProductoApartado.setIdReserva(id);
                    mProductoApartado.setIdProdApartado(rs.getInt("idProdApartado"));
                    mProductoApartado.setNombre(rs.getString("p.NombreProducto"));
                    mProductoApartado.setId(rs.getString("p.idProducto"));
                    mProductoApartado.setPrecio(rs.getDouble("p.precio"));
                    mProductoApartado.setCantidad(rs.getInt("pa.cantidad"));
                    mProductoApartado.setTotal(rs.getDouble("total"));
                    mProductoApartado.setSaldo(rs.getDouble("SALDO"));
                    mListaProductos.add(mProductoApartado);                
                } 
            }catch(SQLException e){
                e.printStackTrace();
            }finally{
                if(rs != null){
                    rs.close();
                }
                if(pst != null){
                    pst.close();
                }
                if(con != null){
                    con.close();
                }
            }
            return mListaProductos;
    }
